let t = true;
let f = false;

// and
console.log("AND operator");
console.log(t&&t);
console.log(t&&f);
console.log(f&&t);
console.log(f&&f);

// or
console.log("OR operator");
console.log(t||t);
console.log(t||f);
console.log(f||t);
console.log(f||f);

// not
console.log("not operator");
console.log(!t);
console.log(!f);