// for loop
console.log("forward loop")
for(let i = 1; i<=5; i++){
    console.log(i);
}


// odd nums 15 to 1
console.log("backward loop")
for(let i = 15; i>=1; i -=2){
    console.log(i);
}
// even 2 to 10
for(let  i = 2; i<=10; i +=2){
    console.log(i);
}
// multiplication table of 5
for(let i = 5; i<=50; i += 5){
    console.log(i);
}
